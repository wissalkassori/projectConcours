    private void bndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bndeleteActionPerformed
        int selectedRow = ListeConcours.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) model.getValueAt(selectedRow, 0);
            if (cs.delete(cs.findById(id))) {
                JOptionPane.showMessageDialog(this, "concours supprim√© avec succ√®s");
                load();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un concours");
        }
    }//GEN-LAST:event_bndeleteActionPerformed

    private void bnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnupdateActionPerformed

        int selectedRow = ListeConcours.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un concours !");
            return;
        }

        int id = (int) model.getValueAt(selectedRow, 0);
        String intitule = txtIntitule.getText().trim();
        String lieu = txtLieu.getText().trim();
        java.util.Date utilDate = txtDate.getDate();

        if (intitule.isEmpty() || lieu.isEmpty() || utilDate == null) {
            JOptionPane.showMessageDialog(this, "Tous les champs doivent √™tre remplis !");
            return;
        }

        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier ce concours ?");
        if (response == JOptionPane.YES_OPTION) {
            Concours c = new Concours(id, intitule, sqlDate, lieu);
            if (cs.update(c)) {
                JOptionPane.showMessageDialog(this, "Concours modifi√© avec succ√®s !");
                load();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification !");
            }
        }
    }//GEN-LAST:event_bnupdateActionPerformed

    private void ListeConcoursMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListeConcoursMouseClicked
        int selectedRow = ListeConcours.getSelectedRow();
        if (selectedRow != -1) {
            try {
                // R√©cup√©ration de l'ID
                id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());

                // R√©cup√©ration de l'intitul√© et du lieu
                txtIntitule.setText(model.getValueAt(selectedRow, 1).toString());
                txtLieu.setText(model.getValueAt(selectedRow, 3).toString());

                // Gestion de la date
                Object value = model.getValueAt(selectedRow, 2); // R√©cup√©rer la valeur de la colonne "Date"
                if (value instanceof Date) {
                    txtDate.setDate((Date) value);
                } else if (value instanceof String) {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    try {
                        Date date = sdf.parse((String) value);
                        txtDate.setDate(date);
                    } catch (ParseException e) {
                        JOptionPane.showMessageDialog(this, "Erreur de format de date !");
                    }
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la s√©lection du concours !");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner un concours !");
        }

    }//GEN-LAST:event_ListeConcoursMouseClicked


