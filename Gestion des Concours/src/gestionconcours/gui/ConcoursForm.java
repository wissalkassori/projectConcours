    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        String intitule = txtIntitule.getText().trim();
        String lieu = txtLieu.getText().trim();
        java.util.Date utilDate = txtDate.getDate();

        if (intitule.isEmpty() || lieu.isEmpty() || utilDate == null) {
            JOptionPane.showMessageDialog(this, "Tous les champs doivent √™tre remplis !");
            return;
        }

        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

        if (cs.create(new Concours(intitule, sqlDate, lieu))) {
            JOptionPane.showMessageDialog(this, "Concours ajout√© avec succ√®s !");
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout du concours !");
            
        }
    }//GEN-LAST:event_bnAddActionPerformed

