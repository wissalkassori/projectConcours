    private void loadResultats() {
        try {
        model.setRowCount(0); // Clear the table before reloading
        for (Resultat r : rs.findAll()) {
            model.addRow(new Object[]{
                r.getConcours().getIntitule(),
                r.getCandidat().getNom() + " " + r.getCandidat().getPrenom(),
                r.getNote()
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des résultats: " + e.getMessage());
        
    }
}
    

