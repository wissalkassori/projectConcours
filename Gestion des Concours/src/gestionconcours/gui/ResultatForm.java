    private void bnenregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnenregistrerActionPerformed
   
          Concours concours = (Concours) listeConcous.getSelectedItem();
    Candidat candidat = (Candidat) listCandidat.getSelectedItem();
    String noteStr = txtNote.getText().trim();

    if (concours == null) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un concours !");
        return;
    }
    if (candidat == null) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un candidat !");
        return;
    }
    if (noteStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez entrer une note !");
        return;
    }

    float note;
    try {
        note = Float.parseFloat(noteStr);
        if (note < 0 || note > 20) {
            JOptionPane.showMessageDialog(this, "La note doit être comprise entre 0 et 20 !");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Veuillez entrer un nombre valide pour la note !");
        return;
    }

    if (rs.create(new Resultat(concours, candidat, note))) {
        JOptionPane.showMessageDialog(this, "Résultat bien enregistré !");
        loadResultats();
        txtNote.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Problème lors de l'enregistrement !");
    }
    }//GEN-LAST:event_bnenregistrerActionPerformed

    
    
